#!/usr/bin/env node
/**
 * Build EPA EJScreen ZCTA records for Michigan from the EPA ArcGIS REST API.
 *
 * Output: src/data/ejscreen.generated.ts (imported by ejscreen.ts after review)
 *
 * Usage (from artifacts/access-mi/):
 *   node scripts/build-ejscreen-zcta.mjs
 *
 * Requires network. Does not run in CI by default.
 * Review generated values before replacing the hand-curated seed.
 */

import { writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = resolve(__dirname, "../src/data/ejscreen.generated.ts");

const MI_STATE_FIPS = "26";
const ARCGIS_JSON =
  "&outFields=ZCTA5CE10,P_PM25_D2,P_OZONE_D2,P_PTRAF_D2,P_DEMOGIDX_2,LOWINCPCT,MINORPCT&f=json&returnGeometry=false";

const url =
  `https://geodata.epa.gov/arcgis/rest/services/OA/EJScreen/MapServer/2/query?` +
  `where=STATEFP%3D%27${MI_STATE_FIPS}%27&outFields=*${ARCGIS_JSON}&resultRecordCount=2000`;

function parse(v) {
  if (v == null || v === "" || v === "None") return null;
  const n = parseFloat(v);
  return Number.isFinite(n) ? n : null;
}

async function main() {
  console.log("Fetching Michigan ZCTA EJScreen records...");
  const res = await fetch(url);
  if (!res.ok) throw new Error(`EPA API ${res.status}`);
  const json = await res.json();
  const features = json.features ?? [];
  if (features.length === 0) {
    console.warn(
      "EPA returned 0 features. The ArcGIS endpoint may be unavailable or the layer id may have changed.",
    );
    console.warn("Manual CSV ingest via seed-ej-screen.ts remains available.");
    process.exit(0);
  }

  const byZcta = new Map();
  for (const f of features) {
    const a = f.attributes ?? {};
    const zcta = String(a.ZCTA5CE10 ?? a.ZCTA ?? "").padStart(5, "0");
    if (!zcta || !zcta.startsWith("4") && !zcta.startsWith("49")) continue;

    const pm25 = parse(a.P_PM25_D2);
    const ozone = parse(a.P_OZONE_D2);
    const traffic = parse(a.P_PTRAF_D2);
    const demog = parse(a.P_DEMOGIDX_2);
    const ej = Math.round(
      Math.max(pm25 ?? 0, ozone ?? 0, traffic ?? 0, demog ?? 0),
    );

    byZcta.set(zcta, {
      zcta,
      ej_index: ej,
      pm25_percentile: pm25 ?? 0,
      ozone_percentile: ozone ?? 0,
      traffic_percentile: traffic ?? 0,
      wastewater_percentile: 0,
      rmp_percentile: 0,
      pct_low_income: parse(a.LOWINCPCT) ?? 0,
      pct_minority: parse(a.MINORPCT) ?? 0,
      pct_less_hs: 0,
      data_year: 2024,
    });
  }

  const records = Object.fromEntries(
    [...byZcta.entries()].sort(([a], [b]) => a.localeCompare(b)),
  );

  const ts = `/**
 * AUTO-GENERATED by scripts/build-ejscreen-zcta.mjs
 * Source: EPA EJScreen ArcGIS MapServer/2 (ZCTA layer)
 * Generated: ${new Date().toISOString()}
 * Records: ${byZcta.size}
 *
 * Do not hand-edit. Re-run the script to refresh.
 */

import type { EjscreenRecord } from "./ejscreen";

export const MICHIGAN_EJSCREEN_GENERATED: Record<string, EjscreenRecord> = ${JSON.stringify(records, null, 2)};
`;

  writeFileSync(OUT, ts, "utf-8");
  console.log(`Wrote ${byZcta.size} ZCTAs to ${OUT}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});