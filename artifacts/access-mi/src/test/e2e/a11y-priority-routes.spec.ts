import { test, expect } from '@playwright/test';
import AxeBuilder from '@axe-core/playwright';
import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const PRIORITY_ROUTES = [
  '/',
  '/find-care',
  '/zip/48201',
  '/compare',
  '/health-map',
  '/tax-comparison',
  '/health-equity-atlas',
  '/data-sources',
  '/methodology',
  '/about',
];

type ViolationRow = {
  route: string;
  id: string;
  impact: string;
  description: string;
  nodes: number;
  help: string;
};

const allViolations: ViolationRow[] = [];

test.describe.configure({ mode: 'serial' });
test.describe('A11y audit — priority routes', () => {
  test.beforeEach(async ({ page }) => {
    // Suppress first-visit onboarding tour so axe doesn't sample the
    // card mid-fade (transparent card reveals crisis-bar coral and
    // produces false-positive contrast violations).
    await page.addInitScript(() => {
      localStorage.setItem('accessmi_tour_seen', 'true');
    });
  });

  for (const route of PRIORITY_ROUTES) {
    test(`axe scan ${route}`, async ({ page }) => {
      await page.goto(route, { waitUntil: 'domcontentloaded' });
      await page.waitForLoadState('networkidle').catch(() => {});

      const results = await new AxeBuilder({ page })
        .withTags(['wcag2a', 'wcag2aa', 'wcag21a', 'wcag21aa'])
        .analyze();

      for (const v of results.violations) {
        allViolations.push({
          route,
          id: v.id,
          impact: v.impact ?? 'unknown',
          description: v.description,
          nodes: v.nodes.length,
          help: v.helpUrl,
        });
      }

      // Log count but do not hard-fail; full report is written in afterAll
      const criticalOrSerious = results.violations.filter(
        (v) => v.impact === 'critical' || v.impact === 'serious'
      );
      console.log(
        `[a11y] ${route}: ${criticalOrSerious.length} critical/serious, ${results.violations.length - criticalOrSerious.length} other`
      );
    });
  }

  test.afterAll(() => {
    const lines = [
      '# Accessibility Violations',
      '',
      `Generated by a11y-priority-routes.spec.ts at ${new Date().toISOString()}`,
      `Total rule-level violations across priority routes: ${allViolations.length}`,
      '',
    ];
    if (allViolations.length === 0) {
      lines.push(
        '**Status: clean.** No critical/serious WCAG 2.0/2.1 A/AA violations detected across the 10 priority routes:',
        '',
        ...PRIORITY_ROUTES.map((r) => `- ${r}`),
      );
    } else {
      lines.push(
        '| Route | Rule | Impact | Nodes | Description |',
        '| --- | --- | --- | --- | --- |',
        ...allViolations.map(
          (v) =>
            `| ${v.route} | [${v.id}](${v.help}) | ${v.impact} | ${v.nodes} | ${v.description.replace(/\|/g, '\\|')} |`
        ),
      );
    }
    const target = join(__dirname, '../../../A11Y_VIOLATIONS.md');
    try {
      mkdirSync(dirname(target), { recursive: true });
      writeFileSync(target, lines.join('\n'), 'utf8');
    } catch {
      // best-effort
    }
  });
});
